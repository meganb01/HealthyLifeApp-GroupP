   // Method to save all the exercises into a .dat file
    private void save(){
        try(ObjectOutputStream oStream= new ObjectOutputStream(new FileOutputStream("exercise.dat"))){
            oStream.writeObject(exercises);  //writes the entire arraylist to a file   
        }
        catch(IOException e){
            JOptionPane.showMessageDialog(this, "Error: "+ e.getMessage());
        }
    }
    //Method to load all the saved exercises when the app starts
    private void load(){
        try(ObjectInputStream oStream= new ObjectInputStream(new FileInputStream("exercise.dat"))){
           exercises=(ArrayList<Exercise>) oStream.readObject(); //read the all saved exercises in arraylist from the file
           
        }
        catch(FileNotFoundException e){
            JOptionPane.showMessageDialog(this,"The data file exercise.dat was not found. Please save before trying to load.");
        }
        catch(ClassNotFoundException e){
            JOptionPane.showMessageDialog(this, "Load Error: "+ e.getMessage());
        }
        catch(IOException e){
           JOptionPane.showMessageDialog(this, "Load Error: "+ e.getMessage());  
        }
    }
    //Method to show all saved exercises in the textarea
    private void display(){
        textDTA.setText(""); //clear the old exercise from text area
        if(exercises.isEmpty()){ //if there is no exercise, display a message
            textDTA.setText("No Exercise to display");
        }
        else {// loop through each exercise that are stored in arraylist
            for(int i=0;i<exercises.size();i++){
                Exercise e= exercises.get(i);
                    textDTA.append(e.printDetails()+"\n\n");// add the exercise in text area
                
            }
            
        }
    }
