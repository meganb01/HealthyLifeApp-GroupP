    private void addABtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addABtnActionPerformed
        // TODO add your handling code here:
        
        try{
             name= nameATF.getText();// getting the name from the text field
             duration= Double.parseDouble(durationATF.getText());
            if(cardioARB.isSelected()){// if the cardio button is selected 
                String intensity=intensityCmb.getSelectedItem().toString();// converting the selection to string
                Cardio c= new Cardio(intensity, name, duration);
                 calories= c.calculateCalories();//calculating the calroies
                 exercises.add(c);//add the cardio object to arraylist
                
            }
            else if(strengthARB.isSelected()){ //if strength is selected 
                int sets=Integer.parseInt(setsTF.getText());
                int reps=Integer.parseInt(repsTF.getText());
                Strength s = new Strength(sets, reps, name, duration);
                calories= s.calculateCalories();
                exercises.add(s);
            }
            else{
                caloriesATF.setText("Select a type!");
                return;
            }
            caloriesATF.setText(String.format("%.2f", calories));// converting the string into double to get it displayed
        }
        catch(Exception e){
            caloriesATF.setText("Error");
        }
    }//GEN-LAST:event_addABtnActionPerformed

